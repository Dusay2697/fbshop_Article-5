package com.spring.fbshop.front.review.dto;

public class ReviewDto {

	private int userNum;
	private int reviewNum;
	private String reviewTitle;
	private String reviewContent;
	private String reviewRegId;
	private String reviewRegDt;
	public int getUserNum() {
		return userNum;
	}
	public void setUserNum(int userNum) {
		this.userNum = userNum;
	}
	public int getReviewNum() {
		return reviewNum;
	}
	public void setReviewNum(int reviewNum) {
		this.reviewNum = reviewNum;
	}
	public String getReviewTitle() {
		return reviewTitle;
	}
	public void setReviewTitle(String reviewTitle) {
		this.reviewTitle = reviewTitle;
	}
	public String getReviewContent() {
		return reviewContent;
	}
	public void setReviewContent(String reviewContent) {
		this.reviewContent = reviewContent;
	}
	public String getReviewRegId() {
		return reviewRegId;
	}
	public void setReviewRegId(String reviewRegId) {
		this.reviewRegId = reviewRegId;
	}
	public String getReviewRegDt() {
		return reviewRegDt;
	}
	public void setReviewRegDt(String reviewRegDt) {
		this.reviewRegDt = reviewRegDt;
	}
	
	
	
	
	
}	